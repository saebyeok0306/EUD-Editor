import fs from 'fs'
import path from 'path'

export function generateEpsFile(buildDir) {
  const epsContent = `// THIS IS AUTOGENERATED BY EUD-EDITOR
// IT WILL BE COMPILED BY EUDDRAFT

function onPluginStart() {
    // 트리거 초기화 영역
}

function beforeTriggerExec() {
    // 커스텀 트리거 로직 삽입 영역
}

function afterTriggerExec() {
}
`
  fs.writeFileSync(path.join(buildDir, 'main.eps'), epsContent, 'utf-8')
}
